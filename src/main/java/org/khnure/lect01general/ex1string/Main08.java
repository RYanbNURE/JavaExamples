package org.khnure.lect01general.ex1string;

public class Main08 {
    public static void main(String[] args) {
        String str = "Hello, World!";
        System.out.println("Upper case: " + str.toUpperCase());  // Виводить: Upper case: HELLO, WORLD!
        System.out.println("Lower case: " + str.toLowerCase());  // Виводить: Lower case: hello, world!
    }
}
// Цей код демонструє перетворення рядка в верхній і нижній регістр.