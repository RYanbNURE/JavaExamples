package org.khnure.lect02branchingloops.ex1branching;

public class Main03 {
        public static void main(String[] args) {
            int score = 85;
            if (score >= 90) {
                System.out.println("Grade A");
            } else if (score >= 80) {
                System.out.println("Grade B");
            } else if (score >= 70) {
                System.out.println("Grade C");
            } else if (score >= 60) {
                System.out.println("Grade D");
            } else {
                System.out.println("Grade F");
            }
        }
    }
// This snippet uses multiple if-else blocks to implement a grading system.
// Цей уривок використовує декілька блоків if-else для реалізації системи оцінювання.
