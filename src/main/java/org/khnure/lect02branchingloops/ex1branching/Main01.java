package org.khnure.lect02branchingloops.ex1branching;

public class Main01 {
    public static void main(String[] args) {
        int number = 20;
        if (number > 10) {
            System.out.println("Number is greater than 10");
        } else {
            System.out.println("Number is not greater than 10");
        }
    }
}
// This snippet demonstrates a simple if-else condition checking if a number is greater than 10.
// Цей уривок демонструє просту умову if-else, перевіряючи, чи число є більшим за 10.
