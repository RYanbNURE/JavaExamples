package org.khnure.lect02branchingloops.ex1branching;

public class Main02 {
    public static void main(String[] args) {
        int number = 75;
        if (number > 50) {
            if (number < 75) {
                System.out.println("Number is between 50 and 75");
            } else {
                System.out.println("Number is 75 or more");
            }
        } else {
            System.out.println("Number is 50 or less");
        }
    }
}
