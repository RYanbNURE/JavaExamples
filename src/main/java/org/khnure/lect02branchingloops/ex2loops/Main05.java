package org.khnure.lect02branchingloops.ex2loops;

public class Main05 {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.println("i = " + i + ", j = " + j);
            }
        }
    }
}
// Demonstrates nested for loops.
// Демонструє вкладені for цикли.