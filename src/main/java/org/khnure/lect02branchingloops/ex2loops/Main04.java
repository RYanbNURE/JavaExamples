package org.khnure.lect02branchingloops.ex2loops;

public class Main04 {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println("i = " + i);
            i++;
        } while (i < 5);
    }
}
// Demonstrates a do-while loop that executes the block at least once.
// Демонструє do-while цикл, який виконує блок принаймні один раз.