package org.khnure.lect02branchingloops.ex2loops;

public class Main06 {
    public static void main(String[] args) {
        int i = 0;
        while (true) {
            if (i == 5) break;
            System.out.println("i = " + i);
            i++;
        }
    }
}
// Demonstrates using break to exit a while loop.
// Демонструє використання break для виходу з while циклу.