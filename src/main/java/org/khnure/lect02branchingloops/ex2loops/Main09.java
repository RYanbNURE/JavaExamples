package org.khnure.lect02branchingloops.ex2loops;

public class Main09 {
    public static void main(String[] args) {
        for (int i = 10; i > 0; i--) {
            System.out.println("i = " + i);
        }
    }
}
// Demonstrates a for loop counting backwards.
// Демонструє for цикл з обратним відліком.