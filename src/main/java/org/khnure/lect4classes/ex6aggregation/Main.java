package org.khnure.lect4classes.ex6aggregation;

// Головний клас для тестування нашої реалізації
public class Main {
    public static void main(String[] args) {
        // Створення об'єкту Engine з потужністю 300 к.с.
        Engine engine = new Engine(300);

        // Ініціалізація об'єкта Car з вже ініціалізованим двигуном
        Car car = new Car(engine);

        // Виведення потужності двигуна автомобіля
        System.out.println("Потужність двигуна автомобіля: " + car.getCarHorsepower() + " к.с.");
    }
}

/*

### Опис коду:
1. **Клас `Engine`**:
   - Має приватну змінну `horsepower`, яка зберігає потужність двигуна.
   - Конструктор ініціалізує цю змінну.
   - Метод `getHorsepower()` повертає значення потужності двигуна.

2. **Клас `Car`**:
   - Має приватну змінну `engine`, яка є об'єктом класу `Engine`.
   - Конструктор ініціалізує цю змінну.
   - Метод `getCarHorsepower()` викликає метод `getHorsepower()` об'єкта `engine` для отримання потужності двигуна.

3. **Головний клас `Main`**:
   - Створює об'єкт `Engine` з потужністю 300 к.с.
   - Ініціалізує об'єкт `Car` з вже ініціалізованим двигуном.
   - Виводить потужність двигуна автомобіля на екран.
 */
